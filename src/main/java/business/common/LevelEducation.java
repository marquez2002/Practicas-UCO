package business.common;

/**
 * Enumeración que representa los niveles de educación requeridos para realizar una actividad.
 * Cada nivel está asociado a un rango de edad específico.
 * 
 * @autor Antonio Jiménez Jiménez
 * @autor Gonzalo Márquez de Torres
 * @autor José María Muñoz López
 * @version 1.0
 */

public enum LevelEducation {
    
    /**
     * Nivel Infantil: dirigido a niños entre 4 y 6 años.
     */
    INFANTIL,
    
    /**
     * Nivel Juvenil: dirigido a niños y jóvenes entre 7 y 12 años.
     */
    JUVENIL,
    
    /**
     * Nivel Adolescente: dirigido a jóvenes entre 13 y 17 años.
     */
    ADOLESCENTE
}
